// Helper function to get correct resource type from role
const getResourceType = (role: string): PersonnelRole => {
  switch (role) {
    case 'foremen': return 'foreman';
    case 'operators': return 'operator';
    case 'drivers': return 'driver';
    case 'stripers': return 'striper';
    case 'laborers': return 'laborer';
    case 'privateDrivers': return 'privateDriver';
    default: return role.slice(0, -1) as PersonnelRole;
  }
};

// Convert personnel data to resources
export function convertPersonnelToResources(): Resource[] {
  const resources: Resource[] = [];
  
  Object.entries(PERSONNEL_DATA).forEach(([role, names]) => {
    names.forEach(name => {
      resources.push({
        id: `${role}-${uuidv4()}`,
        type: getResourceType(role),
        classType: 'employee',
        name,
        onSite: false, // Default to off-site for all personnel
      });
    });
  });
  
  return resources;
}

// Convert equipment data to resources
export function convertEquipmentToResources(): Resource[] {
  const resources: Resource[] = [];
  
  Object.entries(EQUIPMENT_DATA).forEach(([category, items]) => {
    items.forEach(item => {
      // Map category names to correct resource types
      let resourceType: string;
      if (category === 'trucks') {
        resourceType = 'truck';
      } else if (category === 'equipment') {
        resourceType = 'equipment';
      } else {
        // For other categories, remove the 's' suffix (e.g., 'pavers' -> 'paver')
        resourceType = category.slice(0, -1);
      }
      
      resources.push({
        id: `${category}-${uuidv4()}`,
        type: resourceType as 'skidsteer' | 'paver' | 'excavator' | 'sweeper' | 'millingMachine' | 'grader' | 'dozer' | 'payloader' | 'roller' | 'equipment' | 'truck',
        classType: 'equipment',
        name: `${item.model} #${item.unitNumber}`,
        identifier: item.unitNumber,
        model: item.model,
        vin: item.vin,
        location: item.location,
        onSite: false, // Default to off-site for all equipment and vehicles
      });
    });
  });
  
  return resources;
}